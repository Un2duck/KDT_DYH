def print_numbers(a, b, c):
    return print(a, b, c, end='\n\n')

print_numbers(1, 3, 5)
print_numbers(a=1, b=2, c=3)
a=[5, 0, 2]
print_numbers(*a)
# a=[3, 7, 9]
# print(**a) -> 딕셔너리 언패킹하는 방법
print_numbers(*[9, 1, 2])