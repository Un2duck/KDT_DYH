# [실습] 출력하고 싶은 단을 입력받아서 해당 단의 구구단을 출력하세요.
# [출력예시] 2 * 1 = 2
# ------------------------------------------------------------------------------

gugudan=int(input('출력하고 싶은 구구단은? (2~9 사이에서 입력하세요.)'))
nums=range(1,10)
for i in nums:
    print(f'{gugudan} * {i} = {gugudan*i}')